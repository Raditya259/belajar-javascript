/*
Operator Perbandingan
Sumber : https://www.w3schools.com/js/js_comparisons.asp

1. == (sama dengan)
2. === (sama dengan dan sama tipe datanya)
3. != (tidak sama dengan)
4. !== (tidak sama dengan dan tidak sama tipe datanya)
5. > (lebih besar)
6. < (lebih kecil)
7. >= (lebih besar dan sama dengan)
8. <= (lebih kecil dan sama dengan)
*/

let nilai = 89;

if (nilai >= 90 && nilai <= 100) {
    console.log("nilai anda A");
} else if (nilai >= 80 && nilai <= 89) {
    console.log("nilai anda B");
} else if (nilai >= 60 && nilai <= 79) {
    console.log("nilai anda C");
} else if (nilai >= 40 && nilai <= 59) {
    console.log("nilai anda D");
} else if (nilai >= 0 && nilai <= 39) {
    console.log("nilai anda E");
} else {
    console.log("error");
}

